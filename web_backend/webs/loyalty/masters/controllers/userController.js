export const getPincodeDetails = async (req, res, next) => {
    const { pincode } = req.body;

    if (!pincode) {
        return res.status(400).json({
            success: false,
            message: "pincode is required",
        });
    }

    try {
        const where = {};
        where.del = 0;

        const stateData = await prisma.abq_postal_master.findFirst({
            where: {
                pincode: parseInt(pincode, 10),
            },
            orderBy: {
                state_name: "asc",
            },
        });

        if (!stateData) {
            return res.status(404).json({
                success: false,
                message: "No Data Found",
                result: {},
            });
        }

        return res.status(200).json({
            success: true,
            message: `Success`,
            result: stateData,
        });
    } catch (error) {
        return next(error);
    }
};
export const createUser = async (req, res, next) => {
    try {
        const {
            login_type,
            user_type,
            designation_name,
            name,
            email,
            contact_01,
            employee_id,
            date_of_birth,
            date_of_wedding,
            date_of_joining,
            weekly_off,
            state_name,
            district_name,
            city,
            pincode,
            address,
            working_state_name,
            working_district_name,

        } = req.body;
        const existingUser = await prisma.sfa_user.findFirst({
            where: {
                email,
                contact_01
            }
        });

        if (existingUser) {
            return res.status(400).json({
                success: false,
                message: "User already exists",
            });
        }

        const designationExist = await prisma.roles.findFirst({
            where: {
                role_name: designation_name
            }
        })
        if (!designationExist) {
            return res.status(404).json({ status: false, message: 'Designation not found!' })
        }
        const autogeneratedusername = `${name?.split(" ")[0]?.toLowerCase() ?? "user"}_${contact_01?.toString().slice(-4) ?? "0000"}`;

        const user = await prisma.sfa_user.create({
            data: {
                login_type,
                user_type,
                designation_id: Number(designationExist.id),
                designation_name,
                name,
                email,
                contact_01,
                employee_id,
                date_of_birth: date_of_birth ? new Date(date_of_birth) : null,
                date_of_wedding: date_of_wedding ? new Date(date_of_wedding) : null,
                date_of_joining: date_of_joining ? new Date(date_of_joining) : null,
                weekly_off,
                state_name,
                district_name,
                city,
                pincode,
                address,
                working_state_name,
                working_district_name,
                username: autogeneratedusername,
                password: `${name?.split(" ")[0]?.toLowerCase()}_${Date.now().toString().slice(-2)}`,
            },
        });

        res.status(200).json({
            success: true,
            message: "User created or updated successfully!",
            user,
        });

    } catch (error) {
        next(error);
    }
};

export const getAllUsers = async (req, res, next) => {
    const { page = 1, limit = 20, filters = {} } = req.body;
    const skip = (Number(page) - 1) * Number(limit);
    const take = Number(limit);
    try {
        const fieldTypes = {
            status: "boolean",
            date_created: "date",
            name: "string",
            email: "string",
            contact_01: "number",
            employee_id: "string",
            designation_name: "string",
            state_name: "string",
            district_name: "string",
            city: "string",
            pincode: "number",
            user_type: "string",
        };

        const where = buildPrismaFilters(filters, fieldTypes);
        const totalUsers = await prisma.sfa_user.findMany();
        const totalActive = totalUsers.filter(u => u.status === true || u.status === 'true').length;
        const totalInactive = totalUsers.length - totalActive;
        const users = await prisma.sfa_user.findMany({
            where,
            skip,
            take,
            orderBy: { date_created: 'desc' }
        });

        if (!users.length) {
            return res.status(200).json({
                success: false, message: "No users found", count: totalUsers.length,
                totalActive: totalActive - 1,
                totalInactive, data: []
            });
        }

        return res.status(200).json({
            success: true,
            message: "Users fetched successfully!",
            count: totalUsers.length,
            totalActive: totalActive - 1,
            totalInactive,
            data: users,
        });
    } catch (error) {
        next(error)
    }
};
