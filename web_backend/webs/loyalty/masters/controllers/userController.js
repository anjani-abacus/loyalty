export const changeActiveStatus = async (req, res, next) => {
    try {
        const { id } = req.params;
        const { status } = req.body;

        if (!id) {
            return res.status(400).json({ success: false, message: "userId is required" });
        }

        const result = await prisma.sfa_user.update({
            where: { id: Number(id) },
            data: {
                status,
                status_changed_by_id: req.user.id,
                status_changed_by_name: req.user.name,
                status_changed_on: new Date(),
            },
        });

        await redisClient.del(`user:${id}:refreshToken`);
        await redisClient.del(`user:${id}:accessToken`);

        return res.json({ success: true, message: "User Active Status Updated Successfully", data: result });
    } catch (error) {
        next(error)
    }
}